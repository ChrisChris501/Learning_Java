public class currencyConversions {
public static void main(String[] args){
    //Currency
// Holiday in Spain
    double amountInDollars = 500;
    double oneDollarsToEuros = 0.91;
    double convertDollarsToEuros = amountInDollars * oneDollarsToEuros;
    System.out.println(convertDollarsToEuros);

// Buying a Phone Online
    double phoneCostInEuros = 850;
    double oneEurosToDollars = 1.07;
    double phoneCostInDollars = phoneCostInEuros * oneEurosToDollars;
    System.out.println(phoneCostInDollars);

  //  Pocket Money
    double PocketMoneyInPounds = 20;
    double onePoundToYen = 165;
    double pocketMoneyInYen = PocketMoneyInPounds * onePoundToYen;
    System.out.println(pocketMoneyInYen);

}
}
